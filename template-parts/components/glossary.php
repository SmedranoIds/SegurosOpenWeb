	                        <h4 class="card-glossary__title">Selección de Riesgos</h4>
	                        <p class="mb-0">Conjunto de medidas, generalmente e carácter técnico, adoptadas por una Institución Aseguradora en virtud de las cuales la aceptación de los riesgos  está orientada hacia aquellos que ofrecen menos peligrosidad, evitando la cobertura de los que, por poder originar frecuentes siniestros, provocarían un desequilibrio económico en los resultados de la Institución.  </p>
	                    </a>
