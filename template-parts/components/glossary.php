	                        <h4 class="card-glossary__title">Siniestro</h4>
	                        <p class="mb-0">Es la realización del riesgo, es decir cuando se presenta la pérdida o daño  que esta amparando de acuerdo a lo previsto en la póliza y es motivo de indemnización, obligando a la Institución Aseguradora a satisfacer total o parcialmente al Asegurado o a sus Beneficiarios, el capital necesario para resarcir el daño hasta el monto (suma asegurada) señalado en el contrato, por ejemplo un robo, un choque, una enfermedad o accidente, un incendio, etc. </p>
