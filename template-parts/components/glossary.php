	                        <p class="mb-0">Contrato por el cual una persona (Asegurado) pacta con otra (Institución Aseguradora) mediante el pago de una prestación (prima), una contraprestación, para resarcir el daño económico que un acontecimiento futuro e incierto pueda causarle. </p>
