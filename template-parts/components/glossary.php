	                        <p class="mb-0">Al existir desacuerdo entre el Asegurado y la Institución acerca del monto de cualquier pérdida o daño, la cuestión será sometida a dictamen de un perito nombrado de común acuerdo por escrito por ambas partes, pero si no se pusieren de acuerdo en el nombramiento de un solo perito, se designarán dos, uno por cada parte, lo cual se hará en el plazo de diez días, contado a partir de la fecha en que una de ellas hubiere sido requerida por la otra por escrito para que lo hiciere. Antes de empezar sus labores los dos peritos nombrarán un tercero para el caso de discordia. Si una de las partes se negare a nombrar su perito o simplemente no lo hiciere cuando sea requerida por la otra parte o si los peritos no se pusieren de acuerdo en el nombramiento del tercero, será la Autoridad Judicial la que a petición de cualquiera de las partes, hará el nombramiento del perito, del perito tercero o de ambos si así fuere necesario; sin embargo, la Comisión Nacional de Seguros y Fianzas podrá nombrar el perito o perito tercero en su caso, si de común acuerdo las partes así lo solicitaren. El fallecimiento de una de las partes cuando fuere persona física o su disolución si fuere una sociedad, ocurrido mientras se está realizando el peritaje, no anulará ni afectará los poderes o atribuciones del perito o de los peritos o del perito tercero según el caso o si alguno de los peritos de las partes o el tercero falleciere antes del dictamen, será designado otro por quien corresponda (las partes, los peritos, la Autoridad Judicial o la Comisión Nacional de Seguros y Fianzas) para que lo sustituya. Los gastos y honorarios que se originen con motivo del peritaje, serán a cargo de la Institución y del Asegurado por partes iguales, pero cada parte cubrirá los honorarios de su propio perito. </p>
	                        <p class="mb-0">El peritaje a que esta Cláusula se refiere, no significa aceptación de la reclamación por parte de la Institución, simplemente determinará la pérdida que eventualmente estuviere obligada la Institución a resarcir, quedando las partes en libertad de ejercer las acciones y oponer las excepciones correspondientes.</p>
