	                        <h4 class="card-glossary__title">Contrato de seguro</h4>
	                        <p class="mb-0">En general, es el documento o póliza suscrita con una Institución Aseguradora en el que se establecen las normas que han de regular la relación contractual de aseguramiento entre ambas partes (asegurado y aseguradora). Una parte se obliga al pago de una prima para obtener derecho a recibir las indemnizaciones a consecuencia de una pérdida o daño amparada en el mismo. La otra parte se obliga a cubrir dichas indemnizaciones de acuerdo con el clausulado de dicho contrato en dónde generalmente se establecen las condiciones en que deben ocurrir los siniestros, se definen las exclusiones, las limitaciones y las condiciones de pago, temporalidad del compromiso y definiciones de los aspectos relacionados con el seguro. </p>
	                        <p>Las características principales del Contrato de Seguro son: 
	                        	<ul>
	                        		<li>Es un contrato solemne, por cuanto se perfecciona por el otorgamiento de un documento especial denominado póliza y el pago de la contraprestación correspondiente denominada prima. </li>
	                        		<li>Es un contrato bilateral, por cuanto los contratantes se obligan a efectuar prestaciones recíprocas.</li>
	                        		<li>Es un contrato aleatorio, por cuanto que en todos los riesgos que se aseguran, la falta de certeza, al menos con respecto a una de las partes contratantes, forman su esencia.</li>
