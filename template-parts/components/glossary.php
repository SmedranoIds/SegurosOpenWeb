	                        <p class="mb-0">Conjunto de operaciones encaminadas a rescatar personas o bienes materiales durante o después de la ocurrencia de un siniestro. En los seguros de daños, al indemnizar algún bien con daño parcial, el salvamento consiste en que la Institución Aseguradora puede recuperar una parte de la Indemnización con la venta del salvamento. </p>
