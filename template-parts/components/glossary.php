	                        <p class="mb-0">Se da este nombre a la concurrencia de dos o más entidades aseguradoras en la cobertura de un mismo riesgo.</p>

							<p class="mb-0">Cualquier riesgo tiene un límite de indemnización, particularmente en el seguro de daños ese límite no se puede rebasar debido a la capacidad de retención que tenga una aseguradora por su solvencia económica y sus reservas creadas. Cuando un negocio requiere una suma asegurada muy alta que rebasa estos límites surge la figura legal del Coaseguro en dónde otra aseguradora participa conjuntamente con la compañía líder en la aceptación del riesgo en un porcentaje determinado.</p>

							<p class="mb-0">En el seguro de gastos médicos se conoce también como la participación que tiene el asegurado en el monto de los gastos que realice por concepto de un accidente o una enfermedad y generalmente se fija en porcentaje de los gastos.</p>

