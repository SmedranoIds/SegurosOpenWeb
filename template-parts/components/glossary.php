	                        <h4 class="card-glossary__title">Lesión corporal</h4>
	                        <p class="mb-0">Expresión equivalente a daño a las personas, para diferenciarlo del daño material a algún bien.</p>

							<p>La ley de los grandes números y de probabilidades.- Es una ley que definió y nombró el científico francés Poisson y es la que hace posible el desarrollo de la actividad aseguradora. Establece que a mayor número de exposiciones, más se cumplirá la probabilidad; es decir, que cuanto mayor sea el número de exposiciones, tanto menor se desviará el resultado de la probabilidad.</p>

							<p>Por ejemplo, pensemos en una ciudad que tiene 100,000 automóviles en circulación y cada año son robados 30,000. La probabilidad de que se roben uno es de 3/10. En esta sencilla fórmula en el numerador aparece el número de pérdidas y en el denominador aparece el número total de casos expuestos al mismo riesgo. Este principio tan sencillo permite a las aseguradoras conocer mediante la Ley de las probabilidades las posibles pérdidas a las que tendrán que hacer frente y calcular a sí las primas a los asegurados.</p>
